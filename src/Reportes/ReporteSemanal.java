    void consultar(){
        String sql = "SELECT * FROM inventario.reportesemanales;";
        
        try {
            
            conet = con1.getConnection();
            st = conet.createStatement();
            rs = st.executeQuery(sql);
            
            Object[] datos = new Object[6];
            modelo = (DefaultTableModel) Tabla.getModel();
            
            while (rs.next()){
                
                datos [0] = rs.getString("semana");
                datos [1] = rs.getString("mes");
                datos [2] = rs.getInt("anio");
                datos [3] = rs.getInt("ganancias");
                datos [4] = rs.getString("gastos");
                datos [5] = rs.getInt("ganancia_total");
                
                modelo.addRow(datos);
             
            }
            
            Tabla.setModel(modelo);
            
        }catch (Exception e){
            
        }
    } //fin consultar

