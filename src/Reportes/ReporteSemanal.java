    private void TablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMouseClicked
        
        int fila = Tabla.getSelectedRow();
        if (fila == -1){
            
            JOptionPane.showMessageDialog(null, "No se seleciono fila");
            
        } else {
            
            int semana = Integer.parseInt((String)Tabla.getValueAt(fila,0) .toString());
            String mes = (String) Tabla.getValueAt(fila, 1);
            int anio = Integer.parseInt((String)Tabla.getValueAt(fila,2) .toString());
            
            
            txtSemana.setText(""+semana);
            txtMes.setText(""+mes);
            txtAnio.setText(""+anio);
            
        }
        
    }//GEN-LAST:event_TablaMouseClicked

